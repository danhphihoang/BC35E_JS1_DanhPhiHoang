// Mô hình 3 khối:
/** 
 * - Đầu vào:
 *  + soNgayLam: 20
 *  + luongNgay: 100
 * 
 * - Xử lí:
 *  tongLuong = soNgayLam * luongNgay
 * 
 * - Đầu ra: 
 *  => tongLuong = ?
 * 
 * */ 

// ĐẦU VÀO
var soNgayLam = 20;
var luongNgay = 100000;
var tongLuong = 0;

// XỬ LÍ
tongLuong = soNgayLam * luongNgay;

// ĐẦU RA
console.log("Tổng lương là: " + tongLuong);
