// Bài 2: Tính giá trị trung bình

// Mô hình 3 khối:
/** 
 * - Đầu vào:
 *  + soThu1 = 15
 *  + soThu2 = 20
 *  + soThu3 = 10
 *  + soThu4 = 15
 *  + soThu5 = 55
 * 
 * - Xử lí:
 *  trungBinh = (soThu1 + soThu2 +soThu3 + soThu4 + soThu5) / 5
 * 
 * - Đầu ra: 
 *  => trungBinh = ?
 * 
 * */

// ĐẦU VÀO
var soThu1 = 15;
var soThu2 = 20;
var soThu3 = 10;
var soThu4 = 15;
var soThu5 = 55;
var trungBinh = 0;

// XỬ LÍ
trungBinh = (soThu1 + soThu2 +soThu3 + soThu4 + soThu5) / 5;

// ĐẦU RA
console.log("Giá trị trung bình của 5 số là: " + trungBinh);
